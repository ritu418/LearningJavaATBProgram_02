package Exam_11th_Jan;
//Write a Java program to print numbers from 1 to 10 using a do-while loop.
public class Pgm_09_PrintNum {
    public static void main(String[] args) {

        int n = 1;
        do {

            System.out.println(" " + n);
            n++;
        } while (n <= 10);
    }
}
